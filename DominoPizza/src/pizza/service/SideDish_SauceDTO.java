package pizza.service;

public class SideDish_SauceDTO {
	private String sc_no;
	private String sc_name;
	private String sc_price;
	private String sc_img;
	
	public String getSc_no() {
		return sc_no;
	}
	public void setSc_no(String sc_no) {
		this.sc_no = sc_no;
	}
	public String getSc_name() {
		return sc_name;
	}
	public void setSc_name(String sc_name) {
		this.sc_name = sc_name;
	}
	public String getSc_price() {
		return sc_price;
	}
	public void setSc_price(String sc_price) {
		this.sc_price = sc_price;
	}
	public String getSc_img() {
		return sc_img;
	}
	public void setSc_img(String sc_img) {
		this.sc_img = sc_img;
	}
	
	
}
