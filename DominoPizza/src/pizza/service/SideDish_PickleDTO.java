public class SideDish_PickleDTO {
	private String pc_no;
	private String pc_name;
	private String pc_price;
	private String pc_img;
	
	public String getPc_no() {
		return pc_no;
	}
	public void setPc_no(String pc_no) {
		this.pc_no = pc_no;
	}
	public String getPc_name() {
		return pc_name;
	}
	public void setPc_name(String pc_name) {
		this.pc_name = pc_name;
	}
	public String getPc_price() {
		return pc_price;
	}
	public void setPc_price(String pc_price) {
		this.pc_price = pc_price;
	}
	public String getPc_img() {
		return pc_img;
	}
	public void setPc_img(String pc_img) {
		this.pc_img = pc_img;
	}	
}
