package pizza.service;

public class DoughDTO {

	private String dough_no;
	private String dough_name;
	private String d_no;
	private String p_no;
	private String d_price;
	
	
	public String getDough_no() {
		return dough_no;
	}
	public void setDough_no(String dough_no) {
		this.dough_no = dough_no;
	}
	public String getDough_name() {
		return dough_name;
	}
	public void setDough_name(String dough_name) {
		this.dough_name = dough_name;
	}
	public String getD_no() {
		return d_no;
	}
	public void setD_no(String d_no) {
		this.d_no = d_no;
	}
	public String getP_no() {
		return p_no;
	}
	public void setP_no(String p_no) {
		this.p_no = p_no;
	}
	public String getD_price() {
		return d_price;
	}
	public void setD_price(String d_price) {
		this.d_price = d_price;
	}

	
	


}
